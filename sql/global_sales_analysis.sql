SELECT 
    *
FROM
    sales_analysis.apple_global_sales_dataset;


 /** WHAT IS THE TOTAL REVENUE(USD) GENERATED FROM ALL SALES? **/
 
SELECT 
    SUM(revenue_usd) AS total_revenue
FROM
    apple_global_sales_dataset;
    
 
 /** HOW MANY TOTAL UNITS WERE SOLD ACROSS ALL PRODUCTS? **/
 
SELECT 
    SUM(units_sold) AS total_units_sold
FROM
    apple_global_sales_dataset;
 
 
 /** HOW MANY SALES TRANSACTIONS OCCURRED IN THE DATASET? **/
 
SELECT 
    COUNT(*) AS total_sales_transactions
FROM
    apple_global_sales_dataset;
    
 
 /** WHAT ARE THE DISTINCT PRODUCT CATEGORIES AVAILABLE? **/
 
SELECT DISTINCT
    product_category
FROM
    apple_global_sales_dataset;
    
 
 /** WHAT IS THE AVERAGE UNIT PRICE(USD) OF APPLE PRODUCTS? **/
 
SELECT 
    AVG(unit_price_usd) AS average_price
FROM
    apple_global_sales_dataset;
    
 
 /** WHAT ARE THE TOP 5 CITIES WITH THE HIGHEST REVENUE(USD)? **/
 
SELECT 
    city, SUM(revenue_usd) AS total_revenue
FROM
    apple_global_sales_dataset
GROUP BY city
ORDER BY total_revenue DESC
LIMIT 5;

 
/** WHICH COUNTRIES GENERATE THE MOST REVENUE(USD)? **/
 
SELECT 
    country, SUM(revenue_usd) AS total_revenue
FROM
    apple_global_sales_dataset
GROUP BY country
ORDER BY total_revenue DESC;
 
 
/** WHAT IS THE TOTAL REVENUE(USD) GENERATED BY EACH PRODUCT CATEGORY? **/
 
SELECT 
    product_category, SUM(revenue_usd) AS total_revenue
FROM
    apple_global_sales_dataset
GROUP BY product_category
ORDER BY total_revenue DESC;

  
/** WHICH PRODUCTS GENERATED THE HIGHEST REVENUE(USD)? **/
  
SELECT 
    product_name, SUM(revenue_usd) AS total_revenue
FROM
    apple_global_sales_dataset
GROUP BY product_name
ORDER BY total_revenue DESC
LIMIT 10;


/** WHAT IS THE AVERAGE CUSTOMER RATING FROM EACH PRODUCT? **/
   
SELECT 
    product_name, AVG(customer_rating) AS avg_rating
FROM
    apple_global_sales_dataset
GROUP BY product_name
ORDER BY avg_rating DESC;
    
    
/** WHAT IS THE TOTAL REVENUE(USD) GENERATED BY EACH SALES CHANNEL? **/
    
SELECT 
    sales_channel, SUM(revenue_usd) AS total_revenue
FROM
    apple_global_sales_dataset
